能整合推进缓慢的主要原因就是，过去不动产登记基本上是由住建部来负责，而现在的不动产登记由国土部主导，不少地方政府在职能整合上存在顾虑,特别是市县
一级的地方政府，在不动产登记职责和机构整合上面临着部门博弈。
</p>
<p>
	一位接近国土部人士向经济观察报透露，目前市县级不动产登记机构的整合工作受到来自住建部门的压力，进展并不顺利。为此，国土部近期派出督导组，督战地方不动产登记部门的职能整合。
</p>
<p>
	今年3月1日开始实施的《不动产登记暂行条例》明确规定，不动产登记机构应当按照国务院国土资源主管部门的规定设立统一的不动产登记簿。而按照国土
部此前公布的2015年工作计划要求，要在今年年底完成全国范围内的不动产登记机构的整合，整合之后的不动产登记机构正是明年不动产登记的职能部门。
</p>
<p>
	国土部截至今年3月底的统计数据显示，全国300多个地市州盟、2800多个县市区旗中，只有50个地市、101个县完成了不动产登记职责整合，占
比分别不到16%和4%。即使已经完成职责整合的市县，大多数也尚未将相应的登记机构进行整合，具体从事登记的工作人员尚未整合到位。
</p>
